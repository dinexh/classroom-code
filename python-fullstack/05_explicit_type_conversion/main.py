"""Program on Explicit Type Conversion (Type Casting)."""

# int to float
x = 10
print("int to float:", float(x))

# float to int (truncates)
y = 3.99
print("float to int:", int(y))

# str to int
s = "42"
print("str to int:", int(s))

# int to str
n = 100
print("int to str:", str(n), "- type:", type(str(n)))

# str to float
s2 = "3.14"
print("str to float:", float(s2))
